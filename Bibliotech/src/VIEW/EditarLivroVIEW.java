    private void AlterarLivro(){
       
       int id_livro, valor, ano, cod;
        String nome_livro, editora, autor, categoria;
        
       id_livro =  Integer.parseInt(txtID.getText());
       valor =  Integer.parseInt(txtValor.getText());
       ano =  Integer.parseInt(txtAno.getText());
       cod =  Integer.parseInt(txtCodlivro.getText());
       nome_livro =  txtNome.getText();
       editora =  txtEditora.getText();
       autor =  txtAutor.getText();
       categoria =  txtCategoria.getText();
       
       LivrosMODEL objlivrosM = new LivrosMODEL();
       objlivrosM.setNome_livro(nome_livro);
       objlivrosM.setAno_lancamento(ano);
       objlivrosM.setAutor(autor);
       objlivrosM.setCategoria(categoria);
       objlivrosM.setEditora(editora);
       objlivrosM.setId_livros(id_livro);
       objlivrosM.setValor_livro(valor);
       objlivrosM.setCod_livro(cod);
       
       LivrosDAO objlivrosD = new LivrosDAO();
       objlivrosD.EditarLivro(objlivrosM);  
    }
    
